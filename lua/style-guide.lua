-- Set line numbers
vim.opt.number = true

-- vim.opt.relativenumber = true

-- Enable word wrap
vim.opt.wrap = true

-- Use actual tab characters instead of spaces
vim.opt.expandtab = false  -- Don't convert tabs to spaces
vim.opt.tabstop = 4        -- Set tab width to 4 spaces
vim.opt.shiftwidth = 4     -- Indentation level is 4 spaces
vim.opt.softtabstop = 4    -- Makes backspacing behave as expected

-- Disable mouse support
vim.opt.mouse = ""

-- Sync clipboard with system clipboard (optional)
vim.opt.clipboard = "unnamedplus"



